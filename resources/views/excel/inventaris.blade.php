                function convertToRoman($number)
                {
                    $map = [
                        'M' => 1000,
                        'CM' => 900,
                        'D' => 500,
                        'CD' => 400,
                        'C' => 100,
                        'XC' => 90,
                        'L' => 50,
                        'XL' => 40,
                        'X' => 10,
                        'IX' => 9,
                        'V' => 5,
                        'IV' => 4,
                        'I' => 1,
                    ];

                    $result = '';

                    foreach ($map as $roman => $value) {
                        // Repeat the character while the value is less than or equal to the number
                        while ($number >= $value) {
                            $result .= $roman;
                            $number -= $value;
                        }
                    }

                    return $result;
                }
